# Encapsulation is a right to privacy
# In OOP it means, that some of the qualities of the 
# object is accesable only for the object
# It means, you cant modify some of the qualities from
# the outside
# If we want to make it clear, that the attribute should remain 
# untauched we use "_" before the name of attribute.
# We can also use two underscores "__" its called name mangling
# It will add one underscore and name of the class to all calls of the 
# class. It will happed inside the class. Calling the class from outside will 
# be impossible 
